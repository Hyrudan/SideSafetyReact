					<h3 class="inner-title">Gest&atilde;o e Gerenciamento de Riscos</h3>
					<p>Atualmente, existem riscos associados a todos os locais de trabalho e ind&uacute;strias. Em algumas ind&uacute;strias a consequ&ecirc;ncia de um incidente pode ser baixa, como um trabalhador de escrit&oacute;rio que bate seu joelho numa gaveta aberta, em outras ind&uacute;strias um acidente pode ter conseq&uuml;&ecirc;ncias devastadoras.</p>
<p>Por exemplo, um agricultor pode ficar preso sob um trator, ou um oper&aacute;rio de f&aacute;brica pode sofrer o esmagamento de seus membros em um maquin&aacute;rio.</p>
<p>Acidentes criam sofrimento e ang&uacute;stias pessoais, alem de enormes perdas financeiras. Esses acidentes s&atilde;o de grande interesse para todos os stakeholders, acionistas de uma organiza&ccedil;&atilde;o.</p>
<p>A gest&atilde;o de riscos &eacute; definida como a identifica&ccedil;&atilde;o e a avalia&ccedil;&atilde;o dos riscos que colocam em risco a capacidade de ganho de uma empresa. Esses riscos podem ser eliminados ou reduzidos, alterando os processos de trabalho e sabendo como evitar essas situa&ccedil;&otilde;es de risco. Um sistema de gerenciamento de seguran&ccedil;a envolve v&aacute;rios componentes:</p>
<ul class="box-list">
<li>Identifica&ccedil;&atilde;o dos riscos circundantes para o seu neg&oacute;cio.</li>
<li>Avaliar a possibilidade da ocorr&ecirc;ncia desses eventos de risco.</li>
<li>Encontrar m&eacute;todos vi&aacute;veis ‚Äã‚Äãpara enfrentar os riscos.</li>
<li>Supervisionar os processos de gerenciamento de riscos.</li>
</ul>
<p>Independentemente do tamanho da sua organiza&ccedil;&atilde;o, &eacute; importante tomar precau&ccedil;&otilde;es para evitar perdas.</p>
<p>A SideSafety ajuda sua organiza&ccedil;&atilde;o os servi&ccedil;os de gest&atilde;o de riscos, a fim de oferecer a voc&ecirc; a tranq&uuml;ilidade de saber que seus funcion&aacute;rios est&atilde;o seguros e sua empresa est&aacute; protegida. Por meio da implementa&ccedil;&atilde;o, adequa&ccedil;&atilde;o e revis&atilde;o dos processos de gerenciamento de riscos em alinhamento com atendimento &agrave;s ISO e OHSAS. Abrangendo as quatro camadas de riscos operacionais.</p>
<p>Se um perigo n&atilde;o for identificado, sua contribui&ccedil;&atilde;o para o risco n&atilde;o pode ser estimada. Somente aqueles perigos identificados s&atilde;o pass&iacute;veis de serem controlados com seguran&ccedil;a. Nossos engenheiros experientes podem auxiliar na identifica&ccedil;&atilde;o e an&aacute;lise de perigos.</p>
					<img src="images/service/ser_gestao_graph1.jpg"  style="width: 376px">
					<h4 class="thumb-title">WRAC - Workplace Risk Assessment Control</h4>
<p>O WRAC &eacute; uma abordagem proativa para avaliar qualquer uma ou todas as &aacute;reas de um site, e garantir que os riscos sejam compreendidos e controlados a um n&iacute;vel razo&aacute;vel. &Eacute; "um participativo abordagem para identificar potenciais perdas operacionais de produ&ccedil;&atilde;o ou manuten&ccedil;&atilde;o (desenvolvida inicialmente para a ind&uacute;stria mineral) O WRAC &eacute; um m&eacute;todo de avalia&ccedil;&atilde;o de risco qualitativo espec&iacute;fico, projetado para ser aplicado quando uma organiza&ccedil;&atilde;o deseja entender seus riscos e identificar claramente a prioridade ou os riscos mais altos&nbsp; (MDG1010 Risk Management Handbook for the Mining Industry).</p>
<h4 class="thumb-title">HAZOP</h4>
<p>O estudo HAZOP &eacute; usado para verificar a integridade de projetos ou procedimentos com rela&ccedil;&atilde;o &agrave; seguran&ccedil;a e operabilidade. Nossos engenheiros t&ecirc;m uma vasta experi&ecirc;ncia na condu&ccedil;&atilde;o de an&aacute;lises de HAZOP e investigam como os sistemas ou instala&ccedil;&otilde;es se desviam da inten&ccedil;&atilde;o do projeto criando risco para as pessoas e equipamentos. Os HAZOPs t&ecirc;m sido usados com grande sucesso no setor de Petr&oacute;leo, Minera&ccedil;&atilde;o, G&aacute;s e Qu&iacute;mica para manter ativos e opera&ccedil;&otilde;es mais seguros, mais eficientes e mais confi&aacute;veis.</p>
<p style="margin-bottom: 10.0pt; background: white;"><strong><em>FMECA</h4>
<p>Metodologia destinada a identificar potenciais modos de falha para um produto ou processo, avaliar o risco associado a esses modos de falha, classificar os problemas em termos de import&acirc;ncia e identificar e executar a&ccedil;&otilde;es corretivas para tratar das preocupa&ccedil;&otilde;es mais s&eacute;rias.<span style="font-family: 'Calibri',sans-serif; color: #222222;">&nbsp;</span></p>
<h4 class="thumb-title">What-if </h4>
<p>Identifica amea&ccedil;as e perigos.</p>
<p>Perguntas sobre o que poderia dar errado? e sobre o que aconteceria se as coisas derem errado. Este tipo de an&aacute;lise &eacute; uma atividade de brainstorming e &eacute; realizada por pessoas que t&ecirc;m conhecimento sobre as &aacute;reas, opera&ccedil;&otilde;es e processos que podem ser expostos a eventos e condi&ccedil;&otilde;es perigosos.</p>
<h4 class="thumb-title">BowTie</h4>
<p><em>O</em> M&eacute;todo BowTie &eacute; um diagrama que permite visualizar o risco ao qual se esta enfrentando de uma perspectiva &uacute;nica e f&aacute;cil entendimento. O diagrama tem a forma de una gravata borboleta, criando una clara distin&ccedil;&atilde;o entre a gest&atilde;o dos riscos com controles preventivos e reativos. O poder do diagrama BowTie &eacute; entregar uma vis&atilde;o geral dos m&uacute;ltiplas cen&aacute;rios poss&iacute;veis em uma s&oacute; imagem. Permitindo ao mesmo tempo definir controles e suas formas de monitoramento.</p>
